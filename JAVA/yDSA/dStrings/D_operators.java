package yDSA.dStrings;


public class D_operators {
  public static void main(String[] args) {
    System.out.println('a' + 'b'); //ascii values addition
    System.out.println("a" + "b"); //joining
    System.out.println('a' + 3); //ascii of d

    System.out.println("a"+3);//integer will be converted to Integer(class) that will call toString method

  }
}
